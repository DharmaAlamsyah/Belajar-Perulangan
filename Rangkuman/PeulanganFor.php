<?php
// Perulangan For 
for ($i = 0; $i <10; $i++) {              // Variabel $i sebagai counter yang menghitung berapa kali ia akan mengulang
    echo "<h2>Ini perulangan ke-$i</h2>"; // nilai $i = 0. maka perhitungan dimulai dari 0, Lalu, $i < 10. Artinya, perulangan ini akan mengulang sebanyak 10x.
    $i++ adalah nilai $i akan ditambah 1 disetiap kali melakukan perulangan.
}
?>